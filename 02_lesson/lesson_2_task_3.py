import math

def square(side):
    """
    Вычисляет площадь квадрата.
    Для нецелых сторон округляет результат ВВЕРХ.
    """
    area = side ** 2
    return math.ceil(area)  # Ключевое исправление!

# Тестовые примеры
print("Площадь квадрата со стороной 5:", square(5))
print("Площадь квадрата со стороной 2.3:", square(2.3))
print("Площадь квадрата со стороной 3.7:", square(3.7))

